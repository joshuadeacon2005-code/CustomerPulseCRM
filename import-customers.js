// Convert Excel serial date to JavaScript Date
function excelDateToJSDate(serial) {
  if (!serial || typeof serial !== 'number') return null;
  const utc_days = Math.floor(serial - 25569);
  const utc_value = utc_days * 86400;
  const date_info = new Date(utc_value * 1000);
  return new Date(date_info.getFullYear(), date_info.getMonth(), date_info.getDate());
}

// Convert Yes/No to boolean
function yesNoToBoolean(value) {
  if (!value) return false;
  return value.toString().toLowerCase() === 'yes';
}

// Ensure phone is string
function phoneToString(value) {
  if (!value) return null;
  return value.toString();
}

// Map retailer type to match our schema options
function mapRetailerType(type) {
  if (!type) return null;
  
  const mapping = {
    'Baby & Nursery Independent': 'Baby & Nursery Independent/Boutique',
    'Baby & Nursery Multi-site': 'Baby & Nursery Multi-Site',
    'Toy Store Multi-site': 'Toy Store Multi-Site',
    'Therapy': 'Other' // Therapy is not in our standard list, map to Other
  };
  
  return mapping[type] || type;
}

async function importCustomers() {
  const sql = neon(process.env.DATABASE_URL);
  
  console.log(`Importing ${data.length} customers...`);
  
  let successCount = 0;
  let errorCount = 0;
  
  for (const row of data) {
    try {
      // Map fields from Excel to database
      const customer = {
        name: row['Company Name'],
        email: row['Email'] || null,
        phone: phoneToString(row['Phone']),
        contactName: row['Contact Name'] || null,
        contactTitle: row['Contact Title'] || null,
        contactPhone: phoneToString(row['Contact Phone']),
        contactEmail: row['Contact Email'] || null,
        stage: row['Stage'] ? row['Stage'].toLowerCase() : 'lead',
        assignedTo: userMapping[row['Account Manager']] || null,
        personalNotes: row['Personal Notes'] || null,
        registeredWithBC: yesNoToBoolean(row['Registered with BC']),
        ordersViaBC: yesNoToBoolean(row['Orders via BC']),
        firstOrderDate: excelDateToJSDate(row['First Order Date']),
        storeAddress: row['Store Address'] || null,
        retailerType: mapRetailerType(row['Retailer Type']),
        quarterlySoftTarget: row['Quarterly Soft Target'] || null,
        lastContactDate: excelDateToJSDate(row['Last Contact Date']),
        dateOfFirstContact: excelDateToJSDate(row['Date of First Contact']),
        leadGeneratedBy: row['Lead Generated By'] || null,
        country: row['Country'] || null,
        netsuiteUrl: row['NetSuite URL'] || null,
        bloomconnectUrl: row['BloomConnect URL'] || null
      };
      
      // Insert customer
      const result = await sql`
        INSERT INTO customers (
          name, email, phone, contact_name, contact_title, contact_phone, contact_email,
          stage, assigned_to, personal_notes, registered_with_bc, orders_via_bc,
          first_order_date, store_address, retailer_type, quarterly_soft_target,
          last_contact_date, date_of_first_contact, lead_generated_by, country,
          netsuite_url, bloomconnect_url
        ) VALUES (
          ${customer.name}, ${customer.email}, ${customer.phone}, ${customer.contactName},
          ${customer.contactTitle}, ${customer.contactPhone}, ${customer.contactEmail},
          ${customer.stage}, ${customer.assignedTo}, ${customer.personalNotes},
          ${customer.registeredWithBC}, ${customer.ordersViaBC}, ${customer.firstOrderDate},
          ${customer.storeAddress}, ${customer.retailerType}, ${customer.quarterlySoftTarget},
          ${customer.lastContactDate}, ${customer.dateOfFirstContact}, ${customer.leadGeneratedBy},
          ${customer.country}, ${customer.netsuiteUrl}, ${customer.bloomconnectUrl}
        )
        RETURNING id
      `;
      
      const customerId = result[0].id;
      
      // Handle brands if present
      if (row['Brands']) {
        const brandNames = row['Brands']
          .split(/[,\s]+/)
          .map(b => b.trim())
          .filter(b => b && b !== 'all');
        
        // If "all" is specified or there are specific brands, we'll need to create/link them
        // For now, just log them as we might need to create brands first
        if (brandNames.length > 0 && !row['Brands'].toLowerCase().includes('all')) {
          console.log(`  Customer "${customer.name}" has brands: ${brandNames.join(', ')}`);
        }
      }
      
      successCount++;
      console.log(`✓ Imported: ${customer.name} (assigned to ${row['Account Manager'] || 'unassigned'})`);
      
    } catch (error) {
      errorCount++;
      console.error(`✗ Error importing "${row['Company Name']}":`, error.message);
    }
  }
  
  console.log(`\nImport complete: ${successCount} successful, ${errorCount} errors`);
}

